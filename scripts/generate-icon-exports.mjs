import { readFile, readdir, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const rootDir = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const iconsDir = path.join(rootDir, 'src', 'components', 'Icons');
const indexPath = path.join(iconsDir, 'index.ts');

const iconFilePattern = /^[A-Z][A-Za-z0-9]*Icon\.tsx$/;

const files = (await readdir(iconsDir))
  .filter((fileName) => iconFilePattern.test(fileName) && fileName !== 'BaseIcon.tsx')
  .sort((a, b) => a.localeCompare(b));

const exportLines = [];
const invalidIcons = [];

for (const fileName of files) {
  const componentName = fileName.replace(/\.tsx$/, '');
  const source = await readFile(path.join(iconsDir, fileName), 'utf8');

  if (!source.includes(`export const ${componentName}`)) {
    invalidIcons.push(`${fileName} must export const ${componentName}`);
    continue;
  }

  exportLines.push(`export { ${componentName} } from "./${componentName}";`);
}

if (invalidIcons.length > 0) {
  console.error('Icon export generation failed:');
  for (const message of invalidIcons) {
    console.error(`- ${message}`);
  }
  process.exit(1);
}

const contents = `${[
  '// This file is generated by scripts/generate-icon-exports.mjs.',
  '// Run `npm run generate-icon-exports` after adding or removing icon files.',
  'export { BaseIcon, IconSizes as IconSize } from "./BaseIcon";',
  'export type { IconProps } from "./BaseIcon";',
  '',
  ...exportLines,
].join('\n')}\n`;

await writeFile(indexPath, contents);

console.log(`Generated ${path.relative(rootDir, indexPath)} with ${exportLines.length} icon exports.`);
